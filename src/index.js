// Вам необхідно створити конструктор Студента, у якого мають бути властивості:
//     ім'я, прізвище, рік народження, оцінки, відвідуваність, курс. ' +
// 'Кількість оцінок і відвіданих занять залежить від курсу, на якому займається студент. ' +
// 'Так само у Студента є методи: додати оцінку, додати відвідування, отримати середню успішність,' +
// ' отримати середнє відвідування, отримати кількість пройдених занять, змінити курс(мають оновитися дані про курс),' +
// ' а також отримати всю інформацію про студента.
// Конструктор Студента
// Конструктор студента
function Student(імя, прізвище, рік_народження, курс) {
this.імя = імя;
this.прізвище = прізвище;
this.рік_народження = рік_народження;
this.курс = курс;
this.оцінки = [];
this.відвідуваність = [];
}

// Метод для додавання оцінки
Student.prototype.додатиОцінку = function(оцінка) {
    this.оцінки.push(оцінка);
};

// Метод для додавання відвідування
Student.prototype.додатиВідвідування = function(відвідування) {
    this.відвідуваність.push(відвідування);
};

// Метод для отримання середньої успішності
Student.prototype.отриматиСереднюУспішність = function() {
    if (this.оцінки.length === 0) {
        return 0;
    }
    const сума = this.оцінки.reduce((сума, оцінка) => сума + оцінка);
    return сума / this.оцінки.length;
};

// Метод для отримання середньої відвідуваності
Student.prototype.отриматиСереднюВідвідуваність = function() {
    if (this.відвідуваність.length === 0) {
        return 0;
    }
    const сума = this.відвідуваність.reduce((сума, відвідування) => сума + відвідування);
    return сума / this.відвідуваність.length;
};

// Метод для отримання кількості пройдених занять
Student.prototype.отриматиКількістьПройденихЗанять = function() {
    return this.відвідуваність.length;
};

// Метод для зміни курсу
Student.prototype.змінитиКурс = function(новийКурс) {
    this.курс = новийКурс;
};

// Метод для отримання інформації про студента
Student.prototype.отриматиІнформацію = function() {
    return `Ім'я: ${this.імя}, Прізвище: ${this.прізвище}, Рік народження: ${this.рік_народження}, Курс: ${this.курс}, Оцінки: ${this.оцінки.join(', ')}, Відвідуваність: ${this.відвідуваність.join(', ')}`;

        }